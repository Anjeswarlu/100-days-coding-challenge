
public class Literals {

	public static void main(String[] args) {
		int decVal=234;
		int octVal=03265;
		int hexVal=0x1a;
		float f=12.89f;
		double d=123.345454e4;
		System.out.println(decVal);
		System.out.println(octVal);

		System.out.println(f);

		System.out.println(d);

		System.out.println(hexVal);
	}

}
